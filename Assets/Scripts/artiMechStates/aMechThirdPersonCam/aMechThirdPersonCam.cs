using UnityEngine;
using System.Collections;

namespace Artimech
{
    public class aMechThirdPersonCam : stateMachineGame
    {
        public GameObject m_TargetGoal;
        public GameObject m_TargetFollow;
        public GameObject m_Player;
        public GameObject m_CameraGoal;

        public float m_MinDistanceToPlayer = 4.0f;
        public float m_MaxDistanceToPlayer = 6.0f;
        public float m_MaxDistanceToTargetFollow;
        public float m_CameraFollowVelocity = 5.0f;
        public AnimationCurve m_CamApproachCurve;
        public float m_TargetFollowVelocity = 5.0f;
        public AnimationCurve m_TargetApproachCure;

        public float m_FollowToGoalCamVel = 5.0f;
        public AnimationCurve m_FollowToGoalCamApproachCure;

        [Header("Mouse cam move")]
        public float m_CamRotationVelX = 3.0f;
        public float m_CamRotationVelY = 3.0f;


        new void Awake()
        {
            base.Awake();
            CreateStates();
        }

        // Use this for initialization
        new void Start()
        {
            base.Start();
        }

        // Update is called once per frame
        new void Update()
        {
            base.Update();
        }

        new void FixedUpdate()
        {
            base.FixedUpdate();
        }

        /// <summary>
        /// Autogenerated state are created here inside this function.
        /// </summary>
        void CreateStates()
        {

            m_CurrentState = AddState(new camStart(this.gameObject), "camStart");

            //<ArtiMechStates>
            AddState(new camUpdate(this.gameObject), "camUpdate");

        }
    }
}